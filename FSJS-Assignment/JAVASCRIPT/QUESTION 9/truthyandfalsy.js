//     - Write three JavaScript statement example which provide truthy value.

var no = 4;

if (no) {
  console.log("The number is truthy.");
} else {
  console.log("The number is falsy.");
}

var string = "aman";
if (string) {
  console.log("The number is truthy.");
} else {
  console.log("The number is falsy.");
}

var bool = true;
if (bool) {
  console.log("The number is truthy.");
} else {
  console.log("The number is falsy.");
}

